// named function
function perkalian(a, b){
    let result = a + b
    return result
}

// expresive function // arrow function


// anonymous function

const pertambahan = (a, b) => {
    let result = a + b
    return result
}


// named expression function
const pembagian = function bagi(a, b){
    let result = a / b
    return result
}

console.log(perkalian(5, 2));
console.log(pertambahan(5, 2));
console.log(pembagian(5, 2));